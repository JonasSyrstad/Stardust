        [TestMethod()]
        [TestCategory("Enumerable extensions")]
        public void DeleteWhereTest()
        {
            List<int> list = new List<int>();
            list.Add(1);
            list.Add(2);
            list.Add(3);
            list.Add(4);
            int expected = 3;
            list.DeleteWhere(val => val == 2);
            Assert.AreEqual(expected, list.Count);
            Assert.IsFalse(list.Contains(2));
        }
